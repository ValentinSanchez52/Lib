#include "libft.h"

int					nb_words(const char *s, char c)
{
	int		nb;

	nb = 0;
	while (*s)
	{
		if (*s != c)
		{
			nb++;
			while (*s != c && *s)
				s++;
		}
		else
			s++;
	}
	return (nb);
}
